/* global CONFIGURATION_SOURCE, ENV_API_URL, ENV_AWS_CLIENT_ID, ENV_OIDC_AUTHORITY_URL, ENV_OAUTH2_REDIRECT_URL, ENV_END_SESSION_URL, SSI_API_URL, SSI_AWS_CLIENT_ID, SSI_OIDC_AUTHORITY_URL, SSI_OAUTH2_REDIRECT_URL, SSI_END_SESSION_URL */

let API_URL
let AWS_CLIENT_ID
let OIDC_AUTHORITY_URL
let OAUTH2_REDIRECT_URL
let END_SESSION_URL

switch (CONFIGURATION_SOURCE) {
  case 'SSI':
  case 'Caddy':
    // they have been injected into the HTML
    API_URL = SSI_API_URL
    AWS_CLIENT_ID = SSI_AWS_CLIENT_ID
    OIDC_AUTHORITY_URL = SSI_OIDC_AUTHORITY_URL
    OAUTH2_REDIRECT_URL = SSI_OAUTH2_REDIRECT_URL
    END_SESSION_URL = SSI_END_SESSION_URL
    break
  case 'Webpack':
  default:
    API_URL = ENV_API_URL
    AWS_CLIENT_ID = ENV_AWS_CLIENT_ID
    OIDC_AUTHORITY_URL = ENV_OIDC_AUTHORITY_URL
    OAUTH2_REDIRECT_URL = ENV_OAUTH2_REDIRECT_URL
    END_SESSION_URL = ENV_END_SESSION_URL
    break
}

const GlobalConfiguration = {
  API_URL,
  AWS_CLIENT_ID,
  OIDC_AUTHORITY_URL,
  OAUTH2_REDIRECT_URL,
  END_SESSION_URL,
}

export default GlobalConfiguration
